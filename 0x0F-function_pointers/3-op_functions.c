#include "3-calc.h"
#include <stdlib.h>

/**
 * op_add - function to add
 * @a: first integer
 * @b: second integer
 * Return: sum of a and b
 */

int op_add(int a, int b)
{
        return (a + b);
}

/**
 * op_sub - function to subtract
 * @a: first integer
 * @b: second integer
 * Return: sub
 */

int op_sub(int a, int b)
{
        return (a - b);
}

/**
 * op_mul - function to add
 * @a: first integer
 * @b: second integer
 * Return: mul of a and b
 */

int op_mul(int a, int b)
{
        return (a * b);
}

/**
 * op_div - function to add
 * @a: first integer
 * @b: second integer
 * Return: quotient of a and b
 */

int op_div(int a, int b)
{
        return (a / b);
}

/**
 * op_mod - function to add
 * @a: first integer
 * @b: second integer
 * Return: remainder od divider of a and b
 */

int op_mod(int a, int b)
{
        return (a % b);
}

